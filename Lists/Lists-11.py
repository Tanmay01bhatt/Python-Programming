# List Functions

# reverse() = reverses the list without sorting it

list1 = [1, 2, 99, 4, 103]
list1.reverse()
print("Reversing a list", list1)

# index() = finds the first index of the list item

list2 = ["apple", "banana", "grape"]
inx = list2.index("grape")
print("Index is", inx)

# count() = counts te number of times an item ocuurs

list3 = [1, 44, 55, 1, 5, 6]
count1 = list3.count(1)
print(f"'1' has occured {count1} times")
