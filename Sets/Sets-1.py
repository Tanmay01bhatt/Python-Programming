# Sets are un ordered meaning that they are not indexed
# Therefore no duplicate values are allowed in sets
# Sets are semi immutable = can't change the existing values but can add and remove new items

set1 = {"apple", "banana", "cherry"}
print("Sets are unoredered", set1)

# Sets support all datatypes

set2 = {88, 2.0, "Tanmay", True, None}
print("Stes support all datatypes", set2)

# No duplicates
set3 = {4, 6, 7, 8, 4}
print("No duplicattes allowed", set3)

# True == 1 and False == 0
set4 = {22, 44, 66, 0, 55, False}
print("Duplicate datatype", set4)
