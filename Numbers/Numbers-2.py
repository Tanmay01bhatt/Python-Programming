# Type Casting


def integers():
    x = int(1)
    y = int(2.8)
    z = int("3")
    print(x)  # 1
    print(y)  # 2
    print(z)  # 3


def floating():
    x = float(1)
    y = float(2.8)
    z = float("3")
    print(x)  # 1.0
    print(y)  # 2.8
    print(z)  # 3.0


def strings():
    x = str(1)
    y = str(2.8)
    z = str("3")
    print(x)  # '1'
    print(y)  # '2.8'
    print(z)  # '3'


integers()
floating()
strings()
